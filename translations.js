function applyTranslation(lang) {
  const t = i18n[lang];
  if (!t) return;

  document.documentElement.lang = lang;
  if (t.page_title) document.title = t.page_title;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && t.page_description) metaDesc.setAttribute('content', t.page_description);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.getAttribute('data-i18n-html') === 'true') {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  localStorage.setItem('preferred-lang', lang);
}

function initI18n() {
  const saved = localStorage.getItem('preferred-lang');
  const defaultLang = saved || 'en';
  applyTranslation(defaultLang in i18n ? defaultLang : 'en');

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyTranslation(btn.getAttribute('data-lang')));
  });
}

document.addEventListener('DOMContentLoaded', initI18n);
